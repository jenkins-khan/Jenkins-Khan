<h3 data-modal-title="add">Jenkins Khan API Key</h3>

<p>
  Jenkins Khan can export some informations via an API, 
  that other external tools can query, to report the
  statuses of your branches builds.
</p>

<p>
  If you do not currently use such an external tool, you
  do not need to generate an API key.
  <br />In any case, if, later on, you need an API Key, 
  you'll be able to generate one from your configuration 
  page.
</p>