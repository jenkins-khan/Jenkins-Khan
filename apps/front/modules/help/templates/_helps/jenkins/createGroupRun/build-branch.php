<h3 data-modal-title="add">Build Branch Name</h3>

<p>
  This is the name that Jenkins Khan will display for
  your branch.
</p>

<p>
  This name will only be used as an <strong>informative 
  label</strong>, allowing you to use something more 
  user-friendly that the technical name you might usually 
  give to your git branches <em>(because of the naming 
  conventions on your project, for example)</em>.
</p>
